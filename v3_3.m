%%%% 3.3 Formal Definition of Functions

% Los argumento de salida entre corchetes y separados por comas
% Los de entrada entre paréntesis y separados por comas
% Se comienza con la palabra clave 'function'
% Esto es todo en la primera línea de la función
% Si no hay entradas o salidas se pueden omitir
% En total hay 9 casos distintos dependiendo del número de
% paramétros de entrada y de salida
% Se sobreescribes una función integrada la antigua pierde su funcionalidad
% Se guardan en archivos con el mismo nombre
% Terminan con la pablabra 'end'

a = operaciones(5, 8);
[a, b] = operaciones(5, 8)
[a, b, c] = operaciones(5, 8)
[a, b, c, d] = operaciones(5,8)

